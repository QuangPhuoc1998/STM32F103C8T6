#include "main.h"
#include "MY_ILI9341.h"
#include "TSC2046.h"

uint16_t color[4] = {COLOR_RED,COLOR_GREEN,COLOR_BLUE};
uint8_t set_color;

SPI_HandleTypeDef hspi1;
SPI_HandleTypeDef hspi2;
TS_TOUCH_DATA_Def myTS_Handle;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_SPI1_Init(void);
static void MX_SPI2_Init(void);

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_SPI2_Init();
	//
	ILI9341_Init(&hspi1, LCD_CS_GPIO_Port, LCD_CS_Pin, LCD_DC_GPIO_Port, LCD_DC_Pin, LCD_RST_GPIO_Port, LCD_RST_Pin);
	ILI9341_setRotation(2);
	/*-*/
	ILI9341_Fill(COLOR_RED);
	ILI9341_Fill(COLOR_BLUE);
	ILI9341_Fill(COLOR_BLACK);
	ILI9341_Fill(COLOR_GREEN);
	/*-*/
	ILI9341_Fill_Rect(0,0,279,239,COLOR_WHITE);
	ILI9341_Fill_Rect(280,0,319,59,COLOR_RED);
	ILI9341_Fill_Rect(280,60,319,119,COLOR_GREEN);
	ILI9341_Fill_Rect(280,120,319,179,COLOR_BLUE);
	ILI9341_Fill_Rect(280,180,319,239,COLOR_BLACK);
	TSC2046_Begin(&hspi2, TS_CS_GPIO_Port, TS_CS_Pin);
	Calibrate();
  while (1)
  {
		myTS_Handle = TSC2046_GetTouchData();
		if(myTS_Handle.isPressed)
		{
			if(myTS_Handle.X >= 280 && myTS_Handle.X < 320)
			{
				if(myTS_Handle.Y >= 0 && myTS_Handle.Y < 60) set_color = 0;
				if(myTS_Handle.Y >= 60 && myTS_Handle.Y < 120) set_color = 1;
				if(myTS_Handle.Y >= 120 && myTS_Handle.Y < 180) set_color = 2;
				if(myTS_Handle.Y >= 180 && myTS_Handle.Y < 240) ILI9341_Fill_Rect(0,0,279,239,COLOR_WHITE);
			}
			else
			ILI9341_fillCircle(myTS_Handle.X, myTS_Handle.Y,2, color[set_color]);
		}
  }
}
